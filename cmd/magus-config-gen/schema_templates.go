// bindOnlyFields filters out boolptr fields — they are env-only and must not
// appear in BindFlags to avoid corrupting the three-state nil/*bool value.
func bindOnlyFields(defs []FlagDef) []FlagDef {
	out := make([]FlagDef, 0, len(defs))
	for _, s := range defs {
		if s.Kind != "boolptr" {
			out = append(out, s)
		}
	}
	return out
}

// fieldsTmpl generates magus/schema/gen/fields.go (no import of magus/internal/config — pure data).
var fieldsTmpl = template.Must(template.New("fields").Funcs(template.FuncMap{
	"trimCfg": func(s string) string { return strings.TrimPrefix(s, "cfg.") },
	"kindConst": func(k string) string {
		switch k {
		case "string":
			return "KindString"
		case "int":
			return "KindInt"
		case "bool":
			return "KindBool"
		case "float64":
			return "KindFloat64"
		case "boolptr":
			return "KindBoolPtr"
		case "duration":
			return "KindDuration"
		case "stringslice":
			return "KindStringSlice"
		}
		return "KindString"
	},
}).Parse(`// Code generated by cmd/magus-config-gen; DO NOT EDIT.
package gen

func init() {
	Fields = []Field{
{{- range .}}
		{
			GoPath:   "{{trimCfg .GoPath}}",
			YamlPath: "{{.YamlPath}}",
			EnvVar:   "{{.EnvVar}}",
			Flag:     FlagNames{Long: "{{.Flag}}"{{if .FlagShort}}, Short: "{{.FlagShort}}"{{end}}},
			Kind:     {{kindConst .Kind}},
			Usage:    "{{.Usage}}",
		},
{{- end}}
	}
}
`))

// bindTmpl generates magus/cmd/magus/gen/bind.go.
var bindTmpl = template.Must(template.New("bind").Parse(`// Code generated by cmd/magus-config-gen; DO NOT EDIT.
package gen

import (
	"flag"

	"github.com/egladman/magus/internal/config"
)

// BindFlags registers one stdlib flag per config field on fs, storing values
// directly into cfg. Call after config.Load so defaults are already applied;
// flag.Parse will only override flags the user explicitly passes.
//
// Fields with kind "boolptr" use three-way nil/true/false semantics and are
// env-only; they are omitted here to avoid losing the nil state via flag.
//
// Regenerate with: cd magus && go generate ./cmd/magus/...
func BindFlags(fs *flag.FlagSet, cfg *config.Config) {
{{- range .}}
{{- if eq .Kind "string"}}
	fs.StringVar(&{{.GoPath}}, "{{.Flag}}", {{.GoPath}}, "{{.Usage}}")
	{{- if .FlagShort}}
	fs.StringVar(&{{.GoPath}}, "{{.FlagShort}}", {{.GoPath}}, "Short for --{{.Flag}}")
	{{- end}}
{{- else if eq .Kind "int"}}
	fs.IntVar(&{{.GoPath}}, "{{.Flag}}", {{.GoPath}}, "{{.Usage}}")
	{{- if .FlagShort}}
	fs.IntVar(&{{.GoPath}}, "{{.FlagShort}}", {{.GoPath}}, "Short for --{{.Flag}}")
	{{- end}}
{{- else if eq .Kind "bool"}}
	fs.BoolVar(&{{.GoPath}}, "{{.Flag}}", {{.GoPath}}, "{{.Usage}}")
	{{- if .FlagShort}}
	fs.BoolVar(&{{.GoPath}}, "{{.FlagShort}}", {{.GoPath}}, "Short for --{{.Flag}}")
	{{- end}}
{{- else if eq .Kind "float64"}}
	fs.Float64Var(&{{.GoPath}}, "{{.Flag}}", {{.GoPath}}, "{{.Usage}}")
	{{- if .FlagShort}}
	fs.Float64Var(&{{.GoPath}}, "{{.FlagShort}}", {{.GoPath}}, "Short for --{{.Flag}}")
	{{- end}}
{{- else if eq .Kind "duration"}}
	fs.DurationVar(&{{.GoPath}}, "{{.Flag}}", {{.GoPath}}, "{{.Usage}}")
	{{- if .FlagShort}}
	fs.DurationVar(&{{.GoPath}}, "{{.FlagShort}}", {{.GoPath}}, "Short for --{{.Flag}}")
	{{- end}}
{{- end}}
{{- end}}
}
`))

// applyEnvTmpl generates magus/internal/config/gen/env.go (inlines parseBoolEnv to avoid a config back-import).
var applyEnvTmpl = template.Must(template.New("env").Parse(`// Code generated by cmd/magus-config-gen; DO NOT EDIT.
package gen

import (
	"strconv"
	"strings"
	"time"

	"github.com/egladman/magus/internal/config"
)

// ApplyEnv overlays MAGUS_* environment variables on top of cfg.
// getenv is called once per known env var; pass os.Getenv for production use
// or a map lookup in tests.
//
// Regenerate with: cd magus && go generate ./cmd/magus/...
func ApplyEnv(cfg *config.Config, getenv func(string) string) {
{{- range .}}
{{- if eq .Kind "string"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		{{.GoPath}} = v
	}
{{- else if eq .Kind "int"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		if n, err := strconv.Atoi(v); err == nil {
			{{.GoPath}} = n
		}
	}
{{- else if eq .Kind "bool"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		{{.GoPath}} = parseBoolEnv(v, {{.GoPath}})
	}
{{- else if eq .Kind "float64"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		if f, err := strconv.ParseFloat(v, 64); err == nil {
			{{.GoPath}} = f
		}
	}
{{- else if eq .Kind "duration"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		if d, err := time.ParseDuration(v); err == nil {
			{{.GoPath}} = d
		}
	}
{{- else if eq .Kind "boolptr"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		b := parseBoolEnv(v, {{.GoPath}} != nil && *{{.GoPath}})
		{{.GoPath}} = &b
	}
{{- else if eq .Kind "stringslice"}}
	if v := getenv("{{.EnvVar}}"); v != "" {
		parts := strings.Split(v, ",")
		out := parts[:0]
		for _, p := range parts {
			if p = strings.TrimSpace(p); p != "" {
				out = append(out, p)
			}
		}
		{{.GoPath}} = out
	}
{{- end}}
{{- end}}
}

// parseBoolEnv mirrors the helper in package config so this generated file
// has no back-import into config (which would create a cycle).
func parseBoolEnv(v string, fallback bool) bool {
	switch strings.ToLower(v) {
	case "true", "1", "yes":
		return true
	case "false", "0", "no":
		return false
	}
	return fallback
}
`))
