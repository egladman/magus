// Subcommand `types` emits the Buzz `object` mirror of a Go value type in
// github.com/egladman/magus/types, so the Go struct stays the single source of
// truth and the two shapes can never drift. The emitted file is shipped in the
// magus/target module (see internal/spell), so `import "magus/target"` brings
// the type into a magusfile's or spell's scope for annotations and literals.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"reflect"
	"strings"

	"github.com/egladman/magus/types"
)

// registry maps a -type name to the Go struct whose fields the Buzz object mirrors.
// Add an entry here to generate another value type.
var registry = map[string]reflect.Type{
	"Target":        reflect.TypeOf(types.Target{}),
	"Command":       reflect.TypeOf(types.Command{}),
	"Service":       reflect.TypeOf(types.Service{}),
	"Charm":         reflect.TypeOf(types.Charm{}),
	"PatchOp":       reflect.TypeOf(types.PatchOp{}),
	"ExecResult":    reflect.TypeOf(types.ExecResult{}),
	"CommitAuthor":  reflect.TypeOf(types.CommitAuthor{}),
	"Commit":        reflect.TypeOf(types.CommitRecord{}),
	"FileInfo":      reflect.TypeOf(types.FileInfo{}),
	"HttpResponse":  reflect.TypeOf(types.HTTPResponse{}),
	"SemverVersion": reflect.TypeOf(types.SemverVersion{}),
	"URL":           reflect.TypeOf(types.URL{}),
}

func runTypes(args []string) error {
	fs := flag.NewFlagSet("types", flag.ExitOnError)
	typeName := fs.String("type", "", "value type to mirror (e.g. ExecResult)")
	outPath := fs.String("out", "", "output .buzz file path")
	if err := fs.Parse(args); err != nil {
		return err
	}

	rt, ok := registry[*typeName]
	if !ok || *outPath == "" {
		return fmt.Errorf("usage: magus-utils types -type <Name> -out <path.buzz>")
	}

	out, err := emit(*typeName, rt)
	if err != nil {
		return err
	}
	if err := os.WriteFile(*outPath, out, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", *outPath, err)
	}
	return nil
}

// emit renders rt as an `export object <name> { … }` declaration. Each exported
// field becomes a Buzz field with the mapped type and its zero-value default.
func emit(name string, rt reflect.Type) ([]byte, error) {
	var b bytes.Buffer
	fmt.Fprintln(&b, "// Code generated by magus-utils types. DO NOT EDIT.")
	fmt.Fprintln(&b, "//")
	fmt.Fprintf(&b, "// Buzz mirror of github.com/egladman/magus/types.%s, shipped in the\n", name)
	fmt.Fprintln(&b, "// magus/target module. Edit the Go struct and rerun `go generate`, never this file.")
	fmt.Fprintln(&b)
	fmt.Fprintf(&b, "export object %s {\n", name)
	for i := 0; i < rt.NumField(); i++ {
		f := rt.Field(i)
		if !f.IsExported() {
			continue
		}
		// A `buzz:"-"` tag excludes the field from the Buzz object (json:"-" style),
		// keeping it Go-only without making it unexported.
		if f.Tag.Get("buzz") == "-" {
			continue
		}
		bt, def, err := buzzType(f.Type)
		if err != nil {
			return nil, fmt.Errorf("field %s: %w", f.Name, err)
		}
		fmt.Fprintf(&b, "    %s: %s = %s,\n", buzzFieldName(f), bt, def)
	}
	fmt.Fprintln(&b, "}")
	return b.Bytes(), nil
}

// buzzType maps a Go field type to its Buzz type name and zero-value default.
func buzzType(t reflect.Type) (typeName, zero string, err error) {
	switch t.Kind() {
	case reflect.String:
		return "str", `""`, nil
	case reflect.Bool:
		return "bool", "false", nil
	case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64:
		return "int", "0", nil
	case reflect.Float32, reflect.Float64:
		return "double", "0.0", nil
	case reflect.Slice:
		elem, _, eerr := buzzType(t.Elem())
		if eerr != nil {
			return "", "", eerr
		}
		return "[" + elem + "]", "[]", nil
	case reflect.Struct:
		// A struct field maps to the Buzz object of the same name (e.g. ExecResult),
		// which must be generated and ordered before this type in the module source.
		// Its zero value is an empty object literal.
		return t.Name(), t.Name() + "{}", nil
	case reflect.Map:
		// A map field maps to a Buzz `{key: val}` type; the zero value is the empty
		// map literal `{}`. Used by HttpResponse.headers ({str: str}).
		keyT, _, kerr := buzzType(t.Key())
		if kerr != nil {
			return "", "", fmt.Errorf("map key: %w", kerr)
		}
		valT, _, verr := buzzType(t.Elem())
		if verr != nil {
			return "", "", fmt.Errorf("map value: %w", verr)
		}
		return "{" + keyT + ": " + valT + "}", "{}", nil
	default:
		return "", "", fmt.Errorf("unsupported Go type %s", t)
	}
}

// buzzFieldName returns the Buzz field name for a Go struct field. An explicit
// `buzz:"name"` tag wins (needed for initialisms and renames — OK → ok, Path →
// projectPath); otherwise the leading capital is lowercased to match Buzz's
// camelCase convention (Mode → mode, Pattern → pattern).
func buzzFieldName(f reflect.StructField) string {
	if tag := f.Tag.Get("buzz"); tag != "" {
		return tag
	}
	return strings.ToLower(f.Name[:1]) + f.Name[1:]
}
