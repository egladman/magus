// glossaryDocURL is the canonical, hosted magus glossary. MAGUS.md links out to it
// instead of embedding its own copy, so the term definitions have a single source
// of truth on the docs site.
const glossaryDocURL = "https://eli.gladman.cc/magus/glossary/"

// EncodeFragment JSON-marshals v, gzip-compresses (BestCompression) the result,
// and returns a base64url-encoded string (no padding) suitable for use as a
// #data= URL fragment. The browser reverses it with DecompressionStream('gzip').
// gzip header fields are left at their zero values to guarantee byte-stable
// output across calls.
//
// This is the single canonical encoder shared by the render package and
// cmd/magus (graph open --targets and the knowledge-graph #data= path). Both
// feed the same browser decode contract, so byte-for-byte wire-format parity is
// required.
func EncodeFragment(v any) (string, error) {
	raw, err := json.Marshal(v)
	if err != nil {
		return "", err
	}
	return encodeFragmentRaw(raw)
}

// EncodeFragmentRaw gzip-compresses (BestCompression) raw and returns a
// base64url-encoded string (no padding) suitable for use as a #data= URL
// fragment. Use this when the caller has already JSON-marshalled its payload;
// use EncodeFragment when marshalling is also needed.
func EncodeFragmentRaw(raw []byte) (string, error) {
	return encodeFragmentRaw(raw)
}

// encodeFragmentRaw is the inner encoder: gzip BestCompression + base64url (no
// padding). Callers that have already marshalled their payload use this directly.
func encodeFragmentRaw(raw []byte) (string, error) {
	var buf bytes.Buffer
	zw, err := gzip.NewWriterLevel(&buf, gzip.BestCompression)
	if err != nil {
		return "", err
	}
	if _, err := zw.Write(raw); err != nil {
		return "", err
	}
	if err := zw.Close(); err != nil {
		return "", err
	}
	return base64.RawURLEncoding.EncodeToString(buf.Bytes()), nil
}

// stripFragment returns url with any existing fragment (the '#' and everything
// after it) removed. Used to strip a #src= or #data= from explorerURL before
// appending a new #q= fragment, ensuring the resulting URL has exactly one
// fragment and is valid.
func stripFragment(rawURL string) string {
	if i := strings.IndexByte(rawURL, '#'); i >= 0 {
		return rawURL[:i]
	}
	return rawURL
}

// queryCell returns a Markdown inline-code span for query when explorerURL is
// empty, or a Markdown link whose text is the inline-code span and whose target
// is the base explorer URL + "#q=" + percent-encoded query, so clicking the
// cell opens the Graph Explorer pre-seeded with that query. Any existing
// fragment on explorerURL (e.g. a #src= passed from graphExplorerLink) is
// stripped so the #q= is the only fragment and the URL is valid.
//
// Spaces are encoded as %20 (via url.PathEscape) rather than '+' (QueryEscape)
// because the browser decodes the fragment with decodeURIComponent, which turns
// %20 back into a space but leaves '+' as a literal plus character.
func queryCell(query, explorerURL string) string {
	if explorerURL == "" {
		return md.Code(query)
	}
	base := stripFragment(explorerURL)
	return md.Link(md.Code(query), strings.TrimRight(base, "/")+"/#q="+url.PathEscape(query))
}

// WriteTargetGraphMarkdown renders a workspace's MAGUS.md: a routing index, not a
// catalog. It leads with the knowledge-graph "query first" section, then lists
// every project's targets with a one-line summary each, and points the reader at
// the two commands that expand any entry - `magus describe target <name>` for a
// target's fully-evaluated dispatch plan, `magus describe mcp-tools` for the agent
// tool list. The per-target dispatch plan and the Mermaid graphs deliberately do
// not live here: their bulk made the file useless as in-context routing, and both
// are one command (or the Graph Explorer link) away. It is engine- and
// repo-agnostic - `magus describe graph -o markdown` produces it for any magus
// workspace, which is how a project's MAGUS.md is generated and drift-checked.
// Output is deterministic (no timestamps) so it can back a drift gate.
func WriteTargetGraphMarkdown(w io.Writer, out types.TargetGraphOutput, routing *types.KnowledgeRouting, explorerURL string, defaultCharms []string) error {
	var b md.Builder

	b.Heading(1, "Targets")
	b.Comment("Generated by `magus describe graph -o markdown`. Do not edit by hand.")

	// Staleness contract, right under the header: this file is a generated snapshot,
	// only as fresh as the last `generate`. When its counts look stale, the live
	// query tools - not this file - are the source of truth.
	b.Paragraph("Generated by " + md.Code("magus describe graph -o markdown") + " via the generate target. " +
		"If the counts here look stale, trust the live tools (" + md.Code("magus query") + ") over this file.")

	// Default charms apply to every local `magus run`; surfaced only when the
	// workspace config sets them. Omitted entirely when there are none, so the
	// header carries no empty line to read past.
	if len(defaultCharms) > 0 {
		b.Paragraph("Default charms: " + strings.Join(defaultCharms, ", ") +
			" (local runs write; CI strips them with " + md.Code("--no-default-charms") + ").")
	}

	b.Paragraph("A **target** is a named operation (build, test, lint, …) declared as an `export fun` in a " +
		"project's magusfile. This is a routing index: every target with a one-line summary, plus the " +
		"commands that expand any one of them. It is extracted statically from the magusfile source, so it " +
		"stays in lockstep with how the project actually builds.")

	// Route by question, front-loaded as the first section: an agent lands on the
	// exact command before scrolling any table. Static routing text - none of it is
	// derived from the graph.
	b.Heading(2, "Route by question")
	b.Table([]string{"To find out", "Run"}, []md.Align{md.Left, md.Left}, [][]string{
		{"what exists / what relates", md.Code(`magus query "<terms>"`)},
		{"is this file generated", md.Code("magus describe file <path>")},
		{"what a target runs", md.Code("magus describe target <name>")},
		{"what my change affected", md.Code("magus affected ci")},
		{"a failing run's output", md.Code("magus query output <ref>")},
	})

	b.Heading(2, "Quick start")
	b.AlignedCodeBlock("sh", []md.CodeLine{
		{Code: "magus run <target>", Note: "from inside the project directory"},
		{Code: "magus run <target> <path>", Note: "from anywhere in the workspace"},
		{Code: "magus run <target>:<charm>", Note: "change HOW it runs (e.g. lint:rw)"},
	})
	b.Paragraph("Unfamiliar with a term? See the " + md.Link("Glossary", glossaryDocURL) + ".")

	// The bulk this index leaves out is one command away: a target's full
	// evaluated dispatch plan, and the tools exposed to AI agents.
	b.Paragraph("Need the detail this index leaves out? Run " + md.Code("magus describe target <name>") +
		" for a target's fully-evaluated dispatch plan (sources, outputs, spells, command, policy), and " +
		md.Code("magus describe mcp-tools") + " for the tools this workspace exposes to AI agents.")

	// Prefer a picture: a link to the hosted Graph Explorer preloaded with this
	// repo's committed graph.json (emitted only when that link resolves). The
	// interactive view replaces the Mermaid graphs this file used to embed.
	if explorerURL != "" {
		b.Paragraph("Prefer a picture? Explore this graph in the " + md.Link("Graph Explorer", explorerURL) +
			" - an interactive, force-directed view of this repo's committed graph.json (it renders in your browser; nothing is uploaded).")
	}

	if routing != nil {
		writeRouting(&b, *routing, explorerURL)
	}

	for _, p := range nonEmptyProjects(out) {
		b.Heading(2, "Project: "+projectLabel(p))
		if len(p.Cycle) > 0 {
			b.Paragraph("> dependency cycle: " + strings.Join(p.Cycle, " -> "))
		}
		rows := make([][]string, 0, len(p.Nodes))
		for _, n := range catalogOrder(p.Nodes) {
			rows = append(rows, []string{md.Code(n.Name), firstDocLine(n.Doc)})
		}
		b.Table([]string{"Target", "What it does"}, []md.Align{md.Left, md.Left}, rows)
	}

	_, err := b.WriteTo(w)
	return err
}

// firstDocLine returns the first line of a target's doc comment, trimmed, with
// any pipe escaped so it can sit in a Markdown table cell. Empty for an
// undocumented target.
func firstDocLine(doc string) string {
	if i := strings.IndexByte(doc, '\n'); i >= 0 {
		doc = doc[:i]
	}
	return strings.ReplaceAll(strings.TrimSpace(doc), "|", `\|`)
}

// writeRouting emits the "query first" section from the knowledge graph: the
// retrieval verbs, a per-kind entry table, and per-project rows. It routes the
// reader's next action to a magus query (counts + the query to run + a few
// high-degree anchor nodes); it never dumps graph data, so it stays diff-stable
// across routine edits. Generated per-repo and drift-gated like the rest of MAGUS.md.
// When explorerURL is non-empty, the query-string cells are emitted as Markdown
// links that open the Graph Explorer pre-seeded with that query (#q=...).
func writeRouting(b *md.Builder, r types.KnowledgeRouting, explorerURL string) {
	b.Heading(2, "Query first")
	b.Paragraphf("This workspace has a knowledge graph of **%d nodes** and **%d edges** "+
		"(schema v%d). Query it instead of grepping:", r.NodeCount, r.EdgeCount, r.SchemaVersion)
	b.AlignedCodeBlock("sh", []md.CodeLine{
		{Code: `magus query "<terms>"`, Note: "kind:spell, project:pkg/foo, relation:uses, free text, -negation"},
		{Code: "magus explain <node>", Note: "one node: its edges, provenance, blast radius"},
		{Code: "magus path <a> <b>", Note: "how two nodes connect"},
		{Code: "magus graph stats", Note: "god nodes, orphans, doc coverage (MCP: magus_stats)"},
		{Code: "magus graph export -o json", Note: "the whole graph (MCP: magus_query, magus_explain, magus_path)"},
	})

	kindRows := make([][]string, 0, len(r.Kinds))
	for _, k := range r.Kinds {
		kindRows = append(kindRows, []string{
			k.Kind, strconv.Itoa(k.Count), queryCell("magus query kind:"+k.Kind, explorerURL), md.Codes(k.Anchors),
		})
	}
	b.Table([]string{"Kind", "Count", "List them", "Anchors (most connected)"},
		[]md.Align{md.Left, md.Right}, kindRows)

	projectRows := make([][]string, 0, len(r.Projects))
	for _, p := range r.Projects {
		projectRows = append(projectRows, []string{
			p.Path, strconv.Itoa(p.TargetCount), queryCell("magus query project:"+p.Path, explorerURL), md.Codes(p.KeyTargets),
		})
	}
	b.Table([]string{"Project", "Targets", "Scope a query", "Key targets"},
		[]md.Align{md.Left, md.Right}, projectRows)
}

// projectLabel is the heading shown for a project: its path relative to the repo
// root (so a project at the workspace root reads as e.g. `magus`, not the
// ambiguous `.`), falling back to the workspace-relative path outside a repo. The
// bare `.` is never used as a heading — a root project with no better label reads
// as "(workspace root)".
func projectLabel(p types.TargetGraphProject) string {
	return p.Label() // the single "never render '.'" rule now lives on the type
}

// catalogOrder lists a project's targets primary-first: the targets you invoke
// directly (build, test, generate, release, …), then the supporting workers a
// worker being a target with no dependencies of its own that exists only to be
// pulled in by another. Source order is preserved within each group.
func catalogOrder(nodes []types.TargetGraphNode) []types.TargetGraphNode {
	incoming := map[string]bool{}
	for _, n := range nodes {
		for _, d := range n.Dependencies {
			incoming[d] = true
		}
	}
	primary := make([]types.TargetGraphNode, 0, len(nodes))
	var workers []types.TargetGraphNode
	for _, n := range nodes {
		if len(n.Dependencies) == 0 && incoming[n.Name] {
			workers = append(workers, n)
		} else {
			primary = append(primary, n)
		}
	}
	return append(primary, workers...)
}

// targetRoleClasses style target nodes by role in the target dependency graph.
//
// ANTI-DRIFT: the classDef names below ("anchor", "target") are mirrored verbatim
// in console/src/console/graph/mermaid.ts (toMermaid / targets flavor). A rename
// here must be matched there. internal/render/mermaid_drift_test.go asserts this
// in CI.
var targetRoleClasses = []renderClass{
	{Name: "anchor", Style: "fill:#2563eb,color:#ffffff,stroke:#1e40af,stroke-width:2px"},
	{Name: "target", Style: "fill:#e2e8f0,color:#0f172a,stroke:#94a3b8"},
}

// WriteTargetGraphMermaid emits a target dependency graph as a Mermaid flowchart.
// It is the one Mermaid emitter for the target graph: `magus describe graph -o
// mermaid` routes through it. See targetGraphIR for the stage-boxing and
// role-styling rules.
func WriteTargetGraphMermaid(w io.Writer, out types.TargetGraphOutput) error {
	return writeMermaid(w, targetGraphIR(out))
}

// targetGraphIR maps the target dependency graph onto the shared renderGraph. With
// more than one project, each is a subgraph and its node IDs are prefixed (so a
// `build` in two projects stays distinct). Worker targets sharing a trailing
// `-<segment>` (e.g. `man-generate`, `content-generate`) are boxed into a collapsing
// stage subgraph, so an edge lands on the box rather than a worker inside it; the
// bare composite (`generate`) stays loose. Nodes are classed by role: top-level
// targets (nothing depends on them) versus plain targets pulled in as dependencies.
// Edges point in run order — a dependency to the target that needs it.
func targetGraphIR(out types.TargetGraphOutput) renderGraph {
	projects := nonEmptyProjects(out)
	multi := len(projects) > 1
	// LR so the graph reads left-to-right like a pipeline. The extra rank/node
	// spacing gives dagre room to route around the per-target spell boxes (GitHub
	// has no ELK engine to lean on). DOT ignores these layout hints.
	g := renderGraph{DOTName: "targets", Direction: "LR", NodeSpacing: 50, RankSpacing: 80}

	// projGroupOf maps a project path to its wrapper-subgraph id, filled as each
	// project is emitted below; the cross-project edge pass (after the loop) reads
	// the completed map so a dependency on a later-listed project still resolves.
	projGroupOf := make(map[string]string, len(projects))

	for i, p := range projects {
		prefix, projGroup := "", ""
		if multi {
			prefix = fmt.Sprintf("p%d_", i)
			projGroup = fmt.Sprintf("p%d", i)
			projGroupOf[p.Path] = projGroup
			g.Groups = append(g.Groups, renderGroup{ID: projGroup, Label: p.Path})
		}

		known := make(map[string]bool, len(p.Nodes))
		for _, n := range p.Nodes {
			known[n.Name] = true
		}
		incoming := map[string]bool{}
		for _, n := range p.Nodes {
			for _, d := range n.Dependencies {
				if known[d] {
					incoming[d] = true
				}
			}
		}

		stageOf, stageOrder := stageGroups(p.Nodes)
		for _, stage := range stageOrder {
			g.Groups = append(g.Groups, renderGroup{
				ID:       prefix + "stage_" + mermaidID(stage),
				Label:    stage,
				Parent:   projGroup,
				Collapse: true,
			})
		}

		for _, n := range p.Nodes {
			group := projGroup
			if s := stageOf[n.Name]; s != "" {
				group = prefix + "stage_" + mermaidID(s)
			}
			// Every target is a single node coloured by role; spells are not boxed
			// inside it. Two roles only: a top-level target (nothing depends on it — a
			// typical entry point) versus a plain target pulled in as a dependency.
			// Both are runnable; the split is a hint, not a rule.
			role := "target"
			if !incoming[n.Name] {
				role = "anchor"
			}
			g.Nodes = append(g.Nodes, renderNode{
				ID:      prefix + mermaidID(n.Name),
				DOTID:   p.Path + ":" + n.Name,
				Label:   n.Name,
				Shape:   shapeRounded,
				Classes: []string{role},
				Group:   group,
			})
		}
		// Edges point in run order, not dependency order: an arrow from a target's
		// dependency to the target itself, so the graph reads "what runs first" left
		// to right (preflight → … → ci), the inverse of the "depends on" relation.
		for _, n := range p.Nodes {
			to := prefix + mermaidID(n.Name)
			for _, d := range n.Dependencies {
				g.Edges = append(g.Edges, renderEdge{From: prefix + mermaidID(d), To: to})
			}
		}
	}

	// Cross-project edges, in run order: a project a depends on runs first, so its
	// box points at the project that needs it. Endpoints are the wrapper-subgraph
	// ids; an edge from a project that contributed no nodes (dropped) is skipped.
	if multi {
		for _, p := range projects {
			to := projGroupOf[p.Path]
			for _, dep := range p.DependsOn {
				if from, ok := projGroupOf[dep]; ok {
					g.Edges = append(g.Edges, renderEdge{From: from, To: to})
				}
			}
		}
	}

	g.Classes = slices.Clone(targetRoleClasses)
	return g
}

// stageGroups buckets a project's *worker* targets into a collapsing stage
// subgraph named for their shared trailing `-<segment>` (e.g. `man-generate`,
// `content-generate` → a `generate` box). A worker joins stage `seg` only when a
// composite target named `seg` exists *and directly depends on it* — the box reads
// as "the steps `seg` runs", so a same-suffix target the composite does not run
// (e.g. a standalone `pgo-generate`) stays loose rather than landing in the box.
// A stage needs two or more such workers; a lone one (or a unique suffix like
// `release`) stays loose. The composite itself is left out of its own box so an
// edge lands on the box, not a `generate` node nested in a `generate` subgraph.
func stageGroups(nodes []types.TargetGraphNode) (map[string]string, []string) {
	present := make(map[string]bool, len(nodes))
	deps := make(map[string]map[string]bool, len(nodes))
	for _, n := range nodes {
		present[n.Name] = true
		set := make(map[string]bool, len(n.Dependencies))
		for _, d := range n.Dependencies {
			set[d] = true
		}
		deps[n.Name] = set
	}

	members := map[string][]string{} // segment -> workers the composite depends on
	for _, n := range nodes {
		seg := lastSegment(n.Name)
		if n.Name == seg || !present[seg] || !deps[seg][n.Name] {
			continue
		}
		members[seg] = append(members[seg], n.Name)
	}

	stageOf := map[string]string{}
	var order []string
	for seg, ms := range members {
		if len(ms) < 2 {
			continue // a lone boxed worker doesn't earn a subgraph
		}
		order = append(order, seg)
		for _, m := range ms {
			stageOf[m] = seg
		}
	}
	slices.Sort(order)
	return stageOf, order
}

// uniqSorted returns s sorted with duplicates removed.
func uniqSorted(s []string) []string {
	slices.Sort(s)
	return slices.Compact(s)
}

// lastSegment returns the text after the final '-', or the whole name if none.
func lastSegment(name string) string {
	if i := strings.LastIndexByte(name, '-'); i >= 0 {
		return name[i+1:]
	}
	return name
}

// WriteTargetGraphDOT emits the target graph as Graphviz DOT, nodes qualified by
// project path so cross-project name clashes stay distinct.
func WriteTargetGraphDOT(w io.Writer, out types.TargetGraphOutput) error {
	return writeDOT(w, targetGraphIR(out))
}

// nonEmptyProjects drops projects with no extracted targets (e.g. a not-yet-
// supported engine) so they don't clutter the rendered graph.
func nonEmptyProjects(out types.TargetGraphOutput) []types.TargetGraphProject {
	ps := make([]types.TargetGraphProject, 0, len(out.Projects))
	for _, p := range out.Projects {
		if len(p.Nodes) > 0 {
			ps = append(ps, p)
		}
	}
	return ps
}
