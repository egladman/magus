#!/usr/bin/env bash
# setup.sh — materialize the large-monorepo benchmark workspace.
#
# Clones vsavkin/large-monorepo at the pinned SHA into ./gen/repo, lays down the
# magus config (the committed spells/nextjs.tl + a generated magusfile.tl), and
# installs node deps. Idempotent: re-running checks out the pinned SHA again and
# refreshes the magus config. To start completely clean, `rm -rf gen/` first.
#
# Everything lives under gen/, which magus's discovery walk ignores (like the
# fixtures' gen/ dirs) — so the cloned repo's own magusfile is never picked up
# by the surrounding magus workspace, and turbo/nx/lage build the clean checkout
# as-is (their configs already live at the repo root). magus support is purely
# additive (a magusfile + the workspace-local nextjs spell); nothing upstream is
# patched. When upstream moves, bump upstream_sha in versions.lock.
set -euo pipefail

DIR="$(cd "$(dirname "$0")" && pwd)"
GEN="$DIR/gen"
REPO="$GEN/repo"
UPSTREAM="https://github.com/vsavkin/large-monorepo.git"

SHA="$(grep '^upstream_sha=' "$DIR/versions.lock" | cut -d= -f2)"
[[ -n "$SHA" ]] || { echo "setup: upstream_sha missing from versions.lock" >&2; exit 1; }

mkdir -p "$GEN"

if [[ ! -d "$REPO/.git" ]]; then
    echo "==> cloning $UPSTREAM"
    git clone "$UPSTREAM" "$REPO"
fi

echo "==> checking out pinned SHA $SHA"
if ! git -C "$REPO" cat-file -e "$SHA^{commit}" 2>/dev/null; then
    git -C "$REPO" fetch origin
fi
git -C "$REPO" checkout -q "$SHA"

echo "==> writing magus config"
mkdir -p "$REPO/spells"
cp "$DIR/spells/nextjs.tl" "$REPO/spells/nextjs.tl"

# Generate the magusfile: load the nextjs spell, then register each app with the
# depends_on edges its package.json declares (app -> its 20 feature libs), so
# cold/warm/incremental scenarios compare like-for-like with turbo/nx/lage. The
# feature/shared packages are left to auto-detection (ts/js, no build verb), so
# `magus run build` builds only the 5 apps and treats packages as inputs.
cat > "$REPO/magusfile.tl" <<'MAGUSFILE'
-- generated by ../setup.sh — do not edit here; edit setup.sh / spells/nextjs.tl
magus.spell.load("spells/nextjs.tl")

local apps: {string} = {
    "crew",
    "flight-simulator",
    "navigation",
    "ticket-booking",
    "warp-drive-manager",
}

for _, app in ipairs(apps) do
    local deps: {string} = {}
    for i = 0, 19 do
        deps[#deps + 1] = "packages/" .. app .. "/important-feature-" .. tostring(i)
    end
    magus.register("apps/" .. app, {
        spell = {name = "nextjs"},
        depends_on = deps,
    })
end
MAGUSFILE

echo "==> npm install"
( cd "$REPO" && npm install )

echo "==> setup complete: $REPO"
echo "    next: ./bench.sh        (see README.md for tool/scenario selection)"
